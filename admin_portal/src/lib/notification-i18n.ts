/**
 * Get a translated notification string with placeholder replacement.
 * Falls back to English, then Turkish if key is missing.
 */
export function nt(locale: string | undefined, key: string, params?: Record<string, string>): string {
  const loc = (locale || 'de') as NotifLocale;
  let text = translations[loc]?.[key] || translations.en?.[key] || translations.tr?.[key] || key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
  }

  return text;
}

/**
 * Resolve locale from various input formats.
 * Accepts: 'de', 'de-DE', 'de_DE', undefined
 */
export function resolveLocale(locale?: string): NotifLocale {
  if (!locale) return 'de'; // Default to German for LOKMA Germany
  const short = locale.split(/[-_]/)[0].toLowerCase();
  if (short in translations) return short as NotifLocale;
  return 'de';
}
